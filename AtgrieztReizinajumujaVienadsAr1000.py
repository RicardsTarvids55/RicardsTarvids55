x = int(input("Ievadiet skaitli"))
y = int(input("Ievadiet skaitli"))
if x*y == 1000: 
    print ("Skaitļu reizinājums ir 1000")
elif x*y<1000:
    print ("Skaitļu reizinājums ir mazāks par 1000, to reizinājums ir", x*y) 
else:
    print ("Skaitļu reizinājums nav 1000, tādēļ ievadīto skaiļu summa ir", x+y)